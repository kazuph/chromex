export function getUiStrings(locale: UiLocale | string | undefined): UiStrings {
  const translatedLocale = getTranslatedUiLocale(locale);
  if (translatedLocale === "ko") {
    return UI_STRINGS.ko;
  }
  return mergeUiStrings(UI_STRINGS.en, buildCoreLocaleOverride(translatedLocale));
}

